-- get current floor's block, used for displays
rawset(_G, "DNG_returnBlock", function(floor)
	local b = BLOCK_THEBEL
	for i = 1, #blockfloor do
		if floor >= blockfloor[i] and i <= server.difficulty
			b = i
		else
			break
		end
	end
	return b
end)

